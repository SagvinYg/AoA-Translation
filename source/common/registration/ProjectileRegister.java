	private static EntityEntry newEntry(final String name, final Class clazz) {
		final EntityEntryBuilder<? extends Entity> builder = EntityEntryBuilder.create();
		final ResourceLocation resource = new ResourceLocation("aoa3", name);

		return builder.id(resource, GeneralEntityRegister.entityId++).name(resource.getResourceDomain() + "." + resource.getResourcePath()).entity(clazz).tracker(120, 20, true).build();
	}
